# -*- coding: utf-8 -*-
"""
02-boolean.py - 논리값 : 참, 거짓을 표현하는 변수
	ex) 메일링 리스트 가입 여부를 True(참)나 False(거짓)으로 표현.
"""
a = True        # 참을 의미하는 키워드 True를 a에 할당
print(a)

b = False       # 거짓을 의미하는 키워드 False를 b에 할당
print(b)

c = not a       # "not" 키워드는 부정을 의미한다. 참의 부정은 거짓
print(c)

d = not b       # 거짓의 부정은 참
print(d)

